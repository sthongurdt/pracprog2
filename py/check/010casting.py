# como en ejp. anteriores, la conversion se logra mediante funciones de constructor
x = int(1)   # x sera 1
y = int(2.8) # y sera 2
z = int('3') # z sera 3
print(x, y, z)
w = float('4.2') # w sera 4.2
x = float(1)     # x sera 1.0
y = float(2.8)   # y sera 2.8
z = float('3')   # z sera 3.0
print(w, x, y, z)
x = str('s1') # x sera 's1'
y = str(2)    # y sera '2'
z = str(3.0)  # z sera '3.0' 
print(x, y, z)